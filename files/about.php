<h2 style='margin-top:0'>Tentang Aplikasi Jalin USO</h2>

<p><a href='images/overview.png' title='Klik to view actual size'><img src='images/overview.png' alt='overview' style='width:60%;height:63%;border:1px solid #e7e7e7;padding:5px;background:#fff;margin:0 0 0 10px;float:right' /></a>Aplikasi Jalin USO merupakan sebuah aplikasi berbasis web yang dibangun menggunakan bahasa pemrograman PHP yang ditujukan untuk memudahkan pengguna dalam memanage database. Fitur yang tersedia meliputi CRUD (Tambah, View, Edit, Hapus) untuk database dan user, upload foto dokumentasi, pencatatan gangguan, dan pengaturan akun. Aplikasi ini terdiri dari 3 panel utama, yaitu <u>Sidebar Panel</u>, <u>Top Panel</u>, dan <u>Content Panel</u>.</p>
<p><b>Sidebar Panel</b> --> <b><u>Menu Utama</u></b> yang terdiri dari.</p>
<table border='1' cellpadding='5' border-color='#e7e7e7' style='border-collapse:collapse;font-size:1em' >
  <tr>
	  <td width='15%'><b>Home</b></td>
	  <td>Halaman Awal.</td>
  </tr>
  <tr>
	  <td><b>Paket</b></td>
	  <td>Tiap paket memiliki sub menu tersendiri yang berupa daftar provinsi. Klik provinsi tertentu untuk menampilkan daftar lokasi berdasarkan provinsi.</td>
  </tr>
  <tr>
	  <td><b>Gangguan</b></td>
	  <td>Digunakan untuk pencatatan gangguan.</td>
  </tr>
  <tr>
	  <td><b>Setting</b></td>
	  <td>Pengaturan Akun. Fitur yang tersedia yaitu ubah password dan edit username. Super Admin dapat menambah dan menghapus user account.</td>
  </tr>
  <tr>
	  <td><b>Logout</b></td>
	  <td>Keluar dari aplikasi.</td>
  </tr>
</table>
<br><br>
<p><b>Top Panel</b> --> <b><u>Kotak Pencarian</u></b>, <b><u>Informasi Pengguna</u></b>, dan <b><u>Horizontal Menu</u></b></p>

<a href='images/appsearch.png' title='Klik to view actual size'><img src='images/appsearch.png' alt='kotak pencarian' style='width:50%;height:26%;border:1px solid #e7e7e7;padding:5px;background:#fff;margin:0 10px 0 0;float:left' /></a><b><u>Kotak Pencarian</u></b><br>
<p>Fitur pencarian dilengkapi dengan livesearch. Untuk menggunakannya cukup dengan memasukkan <u>no.jaringan</u>, <u>kota</u>, atau <u>alamat</u> yang hendak dicari dan secara otomatis data yang sesuai dengan kata kunci akan ditampilkan. Pilih data yang sesuai kemudian tekan tombol <b>Submit</b> untuk melihat secara detail.</p>

<b><u>Informasi Pengguna</u></b><br>
<p>Berupa informasi username dan tipe akun.</p>

<b><u>Horizontal Menu</u></b><br>
